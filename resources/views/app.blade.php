        <meta name="viewport" content="width=device-width, initial-scale=1">

        {{-- Inline script to prevent FOUC: read VueUse color-mode preference from localStorage --}}
        <script>
            (function() {
                const stored = localStorage.getItem('vueuse-color-scheme');
                const prefersDark = stored === 'dark' || (stored === 'auto' || !stored) && window.matchMedia('(prefers-color-scheme: dark)').matches;

                if (prefersDark) {
                    document.documentElement.classList.add('dark');
                }
            })();
        </script>

        {{-- Inline style to set the HTML background color based on our theme in app.css --}}
        <style>
            html {
                background-color: oklch(1 0 0);
            }

            html.dark {
                background-color: oklch(0.145 0 0);
            }
        </style>

